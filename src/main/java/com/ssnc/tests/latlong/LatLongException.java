package com.ssnc.tests.latlong;

public class LatLongException extends Exception {
	private static final long serialVersionUID = -509427333585829888L;

	public LatLongException() {
		super();
	}

	public LatLongException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public LatLongException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LatLongException(String arg0) {
		super(arg0);
	}

	public LatLongException(Throwable arg0) {
		super(arg0);
	}

}
